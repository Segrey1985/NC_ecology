# ─────────────────────────────────────────────────────────────────────────────
# 6.6  Водные биоресурсы (опциональный раздел)
# ─────────────────────────────────────────────────────────────────────────────

class Aquatic(BaseModel):
    """Сведения о воздействии на водные биоресурсы (раздел 6.6, опциональный)"""

    is_required: bool = Field(
        False,
        description=(
            "Включать ли раздел 6.6 в документ. "
            "True — если участок расположен в водоохранной зоне или "
            "прибрежной защитной полосе водного объекта."
        ),
        json_schema_extra={"vanish": True},
    )
    glavrybvod_report_ref: Optional[str] = Field(
        None,
        description=(
            "Реквизиты отчёта Главрыбвода. "
            "Пример: «№ 2024-СЗФ-001 от 15.03.2024»."
        ),
    )
    appendix_number: Optional[str] = Field(
        None,
        description=(
            "Номер приложения к разделу ООС, содержащего материалы Главрыбвода. "
            "Пример: «3»."
        ),
    )
    water_object_name: Optional[str] = Field(
        None,
        description=(
            "Наименование водного объекта (в родительном падеже). "
            "Пример: «реки Охта»; «Финского залива»."
        ),
    )
    water_object_description: Optional[str] = Field(
        None,
        description=(
            "Описание водного объекта (гидрологическая характеристика). "
            "Пример: «Река Охта — левый приток Невы, длина 90 км…»"
        ),
    )
    ichthyofauna_description: Optional[str] = Field(
        None,
        description=(
            "Описание ихтиофауны водного объекта. "
            "Пример: «В реке Охта обитают плотва, окунь, щука…»"
        ),
    )
    rosrybolovstvo_conclusion_ref: Optional[str] = Field(
        None,
        description=(
            "Реквизиты заключения Росрыболовства. "
            "Пример: «№ 123-СЗ от 20.04.2024»."
        ),
    )
    _part_name: list = PrivateAttr(default=["ИЭИ"])
